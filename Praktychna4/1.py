'''
Матрицю розміром МхМ заповнити відповідно до зразка:
'''
m = ((1, 1, 1, 1, 1, 1),
     (1, 2, 3, 4, 5, 6),
     (1, 3, 6, 10, 15, 21),
     (1, 4, 10, 20, 35, 56),
     (1, 5, 15, 35, 70, 126),
     (1, 6, 21, 56, 126, 252))
r = 5
s = 0
s1 = 0
for i in range(0, r+1):
    s += m[i][i]
    s1 += m[r-i][i]
print(s)
print(s1)