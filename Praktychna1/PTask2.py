'''
Задано натуральне чотиризначне число N. Визначити найбільшу
цифру цього числа.
'''
while True:
    print('Введіть натуральне чотирьохзначне число. Q для виходу: ')
    x = input()
    if x.upper() == 'Q':
        break
    try:
        int(x) # Перевіряємо чи число
    except:
        print("Ви ввели не число")
        continue
    m = x[0]
    for i in x[1:]:
        m = i if int(i) > int(m) else m
    print('Максимальна цифра в цьому числі = %s')
